"""
ULTRAPLAN - Complex Multi-Step Planning System
Deep planning sessions (up to 30 minutes) offloaded to cloud infrastructure

ULTRAPLAN handles complex planning tasks that require:
- Extended reasoning time
- Multi-phase decomposition
- Milestone tracking
- Session persistence and resumption
- Resource-intensive computation
"""
import json
import uuid
from datetime import datetime
from typing import Dict, List, Any, Optional, Callable
from dataclasses import dataclass, field
from enum import Enum
from pathlib import Path
from loguru import logger
from config import settings


class PlanStatus(str, Enum):
    """Status of a planning session"""
    INITIALIZING = "initializing"
    ANALYZING = "analyzing"
    DECOMPOSING = "decomposing"
    PLANNING = "planning"
    REFINING = "refining"
    VALIDATING = "validating"
    COMPLETED = "completed"
    PAUSED = "paused"
    FAILED = "failed"
    CANCELLED = "cancelled"


class MilestoneStatus(str, Enum):
    """Status of a plan milestone"""
    PENDING = "pending"
    IN_PROGRESS = "in_progress"
    COMPLETED = "completed"
    BLOCKED = "blocked"
    SKIPPED = "skipped"


class PlanComplexity(str, Enum):
    """Complexity levels for planning tasks"""
    SIMPLE = "simple"          # < 5 minutes, single-phase
    MODERATE = "moderate"      # 5-15 minutes, multi-phase
    COMPLEX = "complex"        # 15-30 minutes, deep analysis
    EXTENSIVE = "extensive"    # 30+ minutes, may require checkpointing


@dataclass
class PlanMilestone:
    """A milestone within a plan"""
    id: str
    name: str
    description: str
    phase: int
    status: MilestoneStatus = MilestoneStatus.PENDING
    dependencies: List[str] = field(default_factory=list)
    deliverables: List[str] = field(default_factory=list)
    estimated_duration_seconds: int = 300
    actual_duration_seconds: Optional[int] = None
    output: Optional[Dict[str, Any]] = None
    started_at: Optional[datetime] = None
    completed_at: Optional[datetime] = None


@dataclass
class PlanPhase:
    """A phase of the planning process"""
    id: str
    name: str
    description: str
    phase_number: int
    milestones: List[PlanMilestone] = field(default_factory=list)
    status: PlanStatus = PlanStatus.INITIALIZING
    output: Optional[Dict[str, Any]] = None
    started_at: Optional[datetime] = None
    completed_at: Optional[datetime] = None


@dataclass
class UltraPlan:
    """A comprehensive plan generated by ULTRAPLAN"""
    id: str
    title: str
    description: str
    goal: str
    complexity: PlanComplexity
    status: PlanStatus = PlanStatus.INITIALIZING
    phases: List[PlanPhase] = field(default_factory=list)
    context: Dict[str, Any] = field(default_factory=dict)
    constraints: List[str] = field(default_factory=list)
    assumptions: List[str] = field(default_factory=list)
    risks: List[Dict[str, Any]] = field(default_factory=list)
    final_output: Optional[Dict[str, Any]] = None
    total_cost: float = 0.0
    estimated_duration_seconds: int = 0
    actual_duration_seconds: Optional[int] = None
    created_at: datetime = field(default_factory=datetime.now)
    started_at: Optional[datetime] = None
    completed_at: Optional[datetime] = None
    checkpoint_data: Optional[Dict[str, Any]] = None
    metadata: Dict[str, Any] = field(default_factory=dict)


@dataclass
class PlanningSession:
    """An active planning session"""
    session_id: str
    plan: UltraPlan
    current_phase: int = 0
    current_milestone: int = 0
    is_paused: bool = False
    last_checkpoint: Optional[datetime] = None
    error_count: int = 0
    callbacks: Dict[str, Callable] = field(default_factory=dict)


