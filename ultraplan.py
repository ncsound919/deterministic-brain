class UltraPlanEngine:
    """
    ULTRAPLAN - Complex Multi-Step Planning Engine

    Handles deep planning sessions with:
    1. Task complexity analysis
    2. Multi-phase decomposition
    3. Milestone tracking
    4. Session persistence and resumption
    5. Cloud offloading for intensive computation
    """

    def __init__(self, config: Optional[UltraPlanConfig] = None):
        self.config = config or UltraPlanConfig()
        self.active_sessions: Dict[str, PlanningSession] = {}
        self._router = None
        self._ensure_storage()

        logger.info("ULTRAPLAN engine initialized")

    def _get_router(self):
        """Lazy-load router"""
        if self._router is None:
            from router import router
            self._router = router
        return self._router

    def _ensure_storage(self):
        """Ensure storage directory exists"""
        self.config.storage_path.mkdir(parents=True, exist_ok=True)

    async def create_plan(
        self,
        goal: str,
        context: Optional[Dict[str, Any]] = None,
        constraints: Optional[List[str]] = None,
        time_limit_seconds: Optional[int] = None
    ) -> UltraPlan:
        """
        Create a comprehensive plan for a complex goal.

        Args:
            goal: The objective to plan for
            context: Additional context and requirements
            constraints: Constraints to consider
            time_limit_seconds: Maximum planning time

        Returns:
            UltraPlan with phases, milestones, and deliverables
        """
        session_id = str(uuid.uuid4())
        logger.info(f"🎯 ULTRAPLAN: Creating plan for: {goal[:100]}...")

        # Analyze complexity
        complexity = await self._analyze_complexity(goal, context)
        estimated_duration = self._estimate_duration(complexity)

        if time_limit_seconds:
            estimated_duration = min(estimated_duration, time_limit_seconds)

        # Create plan structure
        plan = UltraPlan(
            id=str(uuid.uuid4()),
            title=await self._generate_title(goal),
            description=goal,
            goal=goal,
            complexity=complexity,
            context=context or {},
            constraints=constraints or [],
            estimated_duration_seconds=estimated_duration,
        )

        # Create session
        session = PlanningSession(
            session_id=session_id,
            plan=plan
        )
        self.active_sessions[session_id] = session

        try:
            # Phase 1: Deep Analysis
            plan.status = PlanStatus.ANALYZING
            plan.started_at = datetime.now()

            analysis = await self._analyze_goal(goal, context, constraints)
            plan.assumptions = analysis.get("assumptions", [])
            plan.risks = analysis.get("risks", [])

            # Phase 2: Decomposition
            plan.status = PlanStatus.DECOMPOSING
            phases = await self._decompose_into_phases(goal, analysis, complexity)
            plan.phases = phases

            # Phase 3: Detailed Planning
            plan.status = PlanStatus.PLANNING
            await self._plan_phase_details(plan)

            # Phase 4: Refinement
            plan.status = PlanStatus.REFINING
            await self._refine_plan(plan)

            # Phase 5: Validation
            plan.status = PlanStatus.VALIDATING
            validation = await self._validate_plan(plan)

            if validation.get("is_valid", False):
                plan.status = PlanStatus.COMPLETED
                plan.completed_at = datetime.now()
                plan.actual_duration_seconds = int(
                    (plan.completed_at - plan.started_at).total_seconds()
                )
            else:
                logger.warning(f"Plan validation issues: {validation.get('issues', [])}")

            # Generate final output
            plan.final_output = await self._generate_plan_output(plan)

            # Persist plan
            if self.config.enable_persistence:
                await self._save_plan(plan)

            logger.info(
                f"✅ ULTRAPLAN complete: {plan.title} "
                f"({len(plan.phases)} phases, {plan.actual_duration_seconds}s)"
            )

            return plan

        except Exception as e:
            logger.error(f"ULTRAPLAN error: {e}")
            plan.status = PlanStatus.FAILED
            plan.metadata["error"] = str(e)
            raise

        finally:
            # Cleanup session
            if session_id in self.active_sessions:
                del self.active_sessions[session_id]

